# Tuplas -> É uma estrutura semalhante a listas, entretanto existem diferenças significatis:
# Tuplas são imutáveis, enquanto lista podem sfrer alterações
# Para cria  um Tupla pode se utilizar a classe tuple ou colocando valores separados por vírgula de parenteses.
# exemplos:

frutas = ("laranjas","pera","uva",) # Utiliza-se a virgula no final do ultimo elemento para informar a Python que trata-se de uma Tupla

letras = tuple("python")

numeros = tuple([1, 2, 3, 4])

pais = ("Brasil",) # Utiliza-se a virgula no final do ultimo elemento para informar a Python que trata-se de uma Tupla

