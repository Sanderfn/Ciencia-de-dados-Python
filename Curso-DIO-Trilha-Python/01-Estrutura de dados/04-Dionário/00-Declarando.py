# Um dicionário é um conjunto não-ordenado de pares chave:valor, onde as chaves são únicas em uma dada instância do discionário.
# Dicionários são delimitados por chaves: {}, e contém uma lista de pares:valor separada por vírgulas.

pessoa = {"nome": "Guilherme", "idade":28}

pessoa = dict(nome="Guilherme", idade=28)

pessoa["telefone"] = "3333-1234" #{"nome": "guilherme", "idade":28, "telefone": "3333-1234"}