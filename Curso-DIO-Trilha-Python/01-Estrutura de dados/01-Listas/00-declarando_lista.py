#Exemplo de criação de Listas
frutas = ["laranja","maçã","uva"] #lista criada com colchetes, forma mais usual de criação
print(frutas)

frutas = []  # lista vazia
print(frutas)

letras = list("python") # A função list irá criar uma lista para cada letra da palavra python. o construtor list pede um argumento iterável
print(letras)

numeros = list(range(10)) # Nessa ação a função range irá gerar uma relação com 10 elementos que inicia em 0 e termina em 9
print(numeros)

carro = ["Ferrari","F8",4200000,2020,2900,"São Paulo", True]
print(carro)